class BasicConversions{

    public static void main(String[] args){

        System.out.println("Celsius - to - Farehiet");
        int celsius = 96;
        System.out.println("Celsius: " + celsius);
        System.out.println("Fahrenhiet: " + ((celsius*9/5)+32));

        System.out.println("KiloMeters - to - Meters");
        int km = 10;
        System.out.println("KiloMeters: " + km);
        System.out.println("Meters: " + km*1000);

        System.out.println("KiloGrams - to - Grams");
        int kg = 10;
        System.out.println("KiloGrams: " + kg);
        System.out.println("Grams: " + kg*1000);
    }
}